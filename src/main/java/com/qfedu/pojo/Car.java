package com.qfedu.pojo;

public class Car {
    private Long carId;

    private Long carUid;

    private Long carGid;

    private Long carGcount;

    private Integer carFlag;

    public Long getCarId() {
        return carId;
    }

    public void setCarId(Long carId) {
        this.carId = carId;
    }

    public Long getCarUid() {
        return carUid;
    }

    public void setCarUid(Long carUid) {
        this.carUid = carUid;
    }

    public Long getCarGid() {
        return carGid;
    }

    public void setCarGid(Long carGid) {
        this.carGid = carGid;
    }

    public Long getCarGcount() {
        return carGcount;
    }

    public void setCarGcount(Long carGcount) {
        this.carGcount = carGcount;
    }

    public Integer getCarFlag() {
        return carFlag;
    }

    public void setCarFlag(Integer carFlag) {
        this.carFlag = carFlag;
    }
}